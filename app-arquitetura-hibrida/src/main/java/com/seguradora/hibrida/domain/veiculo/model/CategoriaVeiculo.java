package com.seguradora.hibrida.domain.veiculo.model;

/**
 * Enum representando as categorias de veículos no sistema.
 * 
 * @author Principal Java Architect
 * @since 3.0.0
 */
public enum CategoriaVeiculo {
    
    /**
     * Veículo de passeio (carros comuns).
     */
    PASSEIO,
    
    /**
     * Veículo utilitário (SUV, picapes, vans).
     */
    UTILITARIO,
    
    /**
     * Motocicleta (motos, scooters).
     */
    MOTOCICLETA,
    
    /**
     * Caminhão (veículos de carga).
     */
    CAMINHAO
}
